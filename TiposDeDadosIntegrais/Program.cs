//O valor padrão de todos os tipos numéricos do C# é 0.

//-128 a 127
sbyte n3 = -1;

//0 a 127
byte n4 = 1;

//-32.768 a 32.767
short n5 = -1;

//0 a 65.535
ushort n6 = 1;

//inteiro que permite negativo
int n7 = -1;

//inteiro que não permite negativo
uint n8 = 1;

//long que permite negativo
long n9 = -1;

//long que não permite negativo
ulong n10 = 1;

//inteiro com sinal de 32 bits ou 64 bits
nint n11 = 1;

//inteiro sem sinal de 32 bits ou 64 bits
nuint n12 = 1;
