/* O valor padrão dos tipos de dados ponto flutuante é zero(0).
 * São tipos de valor armazenados na stack.
 * Representam números reais.
 * Dão suporte a operadores aritméticos de comparação (>, <, >=, <=, !=) e de igualdade (==).
 * O tipo double é usado para cálculos científicos.
 * O tipo decimal é usado para cálculos financeiros.
 * */


//Precisão de 6 a 9 dígitos - 4 bytes - literais (f-F)
float n1 = 0.0f;

//Precisão de 15 a 17 dígitos - 8 bytes - literais (d-D)
double n2 = 0.0;

//Precisão de 28 a 29 dígitos - 16 bytes - literais (m-M)
decimal n3 = 0.0M;
